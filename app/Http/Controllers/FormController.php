    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'form' => 'required'
        ]);

        $data = $request->form;
        Forms::create([
            'form' => $data
        ]);
        $words = str_word_count($data, 1); // use this function if you only want ASCII
        // $words = utf8_str_word_count($data, 1); // use this function if you care about i18n

        $frequency = array_count_values($words);
        arsort($frequency);
        // ddd($frequency);

        return view('form',compact('data','frequency'));
    }

